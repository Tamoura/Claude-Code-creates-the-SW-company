async function main() {
  console.log(`Seeding ${allMilestones.length} milestone definitions...`);

  let created = 0;
  let updated = 0;

  for (const milestone of allMilestones) {
    const result = await prisma.milestoneDefinition.upsert({
      where: {
        dimension_ageBand_sortOrder: {
          dimension: milestone.dimension,
          ageBand: milestone.ageBand,
          sortOrder: milestone.sortOrder,
        },
      },
      create: {
        dimension: milestone.dimension,
        ageBand: milestone.ageBand,
        title: milestone.title,
        titleAr: milestone.titleAr,
        description: milestone.description,
        descriptionAr: milestone.descriptionAr,
        guidance: milestone.guidance,
        guidanceAr: milestone.guidanceAr,
        sortOrder: milestone.sortOrder,
      },
      update: {
        title: milestone.title,
        titleAr: milestone.titleAr,
        description: milestone.description,
        descriptionAr: milestone.descriptionAr,
        guidance: milestone.guidance,
        guidanceAr: milestone.guidanceAr,
      },
    });

    // Check if this was a create or update by comparing createdAt timestamps
    // (a rough heuristic; upsert always returns the record)
    created++;
  }

  console.log(`Seeding complete. Processed ${created} milestones.`);
  console.log(`Total milestone definitions in DB: ${await prisma.milestoneDefinition.count()}`);
}

main()
  .catch((error) => {
    console.error('Seed failed:', error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
