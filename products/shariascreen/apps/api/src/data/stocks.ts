/**
 * Look up a stock by ticker from sample data.
 */
export function findStockByTicker(
  ticker: string
): ScreeningInput | undefined {
  return SAMPLE_STOCKS.find(
    (s) => s.ticker.toUpperCase() === ticker.toUpperCase()
  );
}

/**
 * Look up multiple stocks by ticker from sample data.
 */
export function findStocksByTickers(
  tickers: string[]
): { found: ScreeningInput[]; notFound: string[] } {
  const found: ScreeningInput[] = [];
  const notFound: string[] = [];

  for (const ticker of tickers) {
    const stock = findStockByTicker(ticker);
    if (stock) {
      found.push(stock);
    } else {
      notFound.push(ticker);
    }
  }

  return { found, notFound };
}
